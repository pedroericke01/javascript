/*
aprendendo estrutura de repetição com variavel de controle:(for), em javascript:
vamos comer uma pizza de 12 fatias e no final da pizza dizer: 'a pizza acabou!'
*/

for (quant_fatias = 12; quant_fatias > 0; quant_fatias -= 1){
    console.log(`Total de fatias:${quant_fatias}`)

    console.log('Essa pizza é minha favorita!\n')   
}

console.log('Acabou a pizza!')

console.log('Fim do programa!')
