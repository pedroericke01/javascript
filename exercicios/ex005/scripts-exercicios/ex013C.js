//realizando a soma de 2 numeros a partir de um procedimento//

//atribuindo n1 e n2 como 2 parâmetros formais que deverão ser informados na chamada
// do procedimento somar() para executar tal procedimento corretamente://
function somar(n1, n2){
    let soma = (n1+n2)
    console.log(`A soma de ${n1} + ${n2} será ${soma}`)
}

num1 = 4
num2 = 6

//fazendo a chamada e passagem dos parâmetros reais do procedimento//
somar(num1, num2)
